    private void estadisticasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_estadisticasActionPerformed
        // TODO add your handling code here:
        String bloque= String.valueOf(modulos.getSelectedItem());
        int bitsControl=archivo.getCantBitsControl(bloque);
        int bitsInfo= archivo.getCantBitsInfo(bloque);
        long cantModulos = archivo.getCantModulos(bloque, arD);
        
        JOptionPane.showMessageDialog(null, "\n"
            + "- Bloque de Hamming: " + bloque+" Bits\n"
            + "- Tamaño del archivo original: " + tamano(ar) + " Bytes\n"
            + "- Tamaño del archivo protegido: " + tamano(arD) + " Bytes\n"
            + "- Cantidad de bits de control por módulo (+ bit de paridad): " + bitsControl+"\n"
            + "- Cantidad de bits de información por módulo: " + bitsInfo+"\n"
            + "- Cantidad de módulos para este archivo: " + cantModulos+"\n"
            + "- Cantidad total de bits de control: " + (bitsControl * cantModulos)+"\n"
            + "- Cantidad total de bits de información: " + (bitsInfo * cantModulos)
                
            ,"ETADÍSTICAS OBTENIDAS", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_estadisticasActionPerformed

    public static long tamano(File archivo) {
        try {
            return Files.size(Paths.get(archivo.getAbsolutePath()));
        } catch (IOException e) {
            e.printStackTrace();
        }
        return -1;
    }
