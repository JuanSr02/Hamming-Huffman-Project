    private void protegerBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_protegerBActionPerformed
        // TODO add your handling code here:
        
        if(ruta_antes.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Debe seleccionar un archivo","Error", JOptionPane.ERROR_MESSAGE);
            return;
        }if(!SiE2.isSelected() && !NoE.isSelected() && !SiE1.isSelected()){
            JOptionPane.showMessageDialog(null,"Debe seleccionar con o sin errores","Error", JOptionPane.ERROR_MESSAGE);    
        }else{
            
             try {
                String nuevaRuta;
                ar = archivo.archivoSeleccionado;
                String ext = archivo.getExtension();
                String ruta =  ar.getAbsolutePath();
                
                
                //Lee el archivo, lo decodifica y lo guarda en la nueva ruta con la nueva extension
                int errores;
                if(SiE1.isSelected()){
                    errores=1;
                }else if(SiE2.isSelected()){
                    errores=2;
                }else{
                    errores=0;
                }
                String bloque= String.valueOf(modulos.getSelectedItem());
                int hamming = Integer.parseInt(bloque);
                arD = ProtegerHamming.protegerArchivo(hamming, ruta,errores);
           
                
                FileReader fr= new FileReader(arD);
                BufferedReader br = new BufferedReader(fr);
                String texto="";
                String linea="";
                while(((linea=br.readLine())!=null)){
                    texto+=linea+"\n";
                }
                DESPUES.setText(texto); //muestra el texto del archivo
                ruta_despues.setText(arD.getAbsolutePath());
                estadisticas.setEnabled(true);
            
            
             } catch (IOException ex) {
                 Logger.getLogger(DesprotegerFrame.class.getName()).log(Level.SEVERE, null, ex);
             }
            
        }
        
    }//GEN-LAST:event_protegerBActionPerformed

